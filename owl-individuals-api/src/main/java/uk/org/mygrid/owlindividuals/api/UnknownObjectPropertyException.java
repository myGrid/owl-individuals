package uk.org.mygrid.owlindividuals.api;

public class UnknownObjectPropertyException extends BuilderException {



	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public UnknownObjectPropertyException() {
		super();
	}

	public UnknownObjectPropertyException(String message, Throwable cause) {
		super(message, cause);
	}

	public UnknownObjectPropertyException(String message) {
		super(message);
	}

	public UnknownObjectPropertyException(Throwable cause) {
		super(cause);
	}

}

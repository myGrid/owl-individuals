package uk.org.mygrid.owlindividuals.api;

public class UnknownDataPropertyException extends BuilderException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public UnknownDataPropertyException() {
		super();
	}

	public UnknownDataPropertyException(String message, Throwable cause) {
		super(message, cause);
	}

	public UnknownDataPropertyException(String message) {
		super(message);
	}

	public UnknownDataPropertyException(Throwable cause) {
		super(cause);
	}

}

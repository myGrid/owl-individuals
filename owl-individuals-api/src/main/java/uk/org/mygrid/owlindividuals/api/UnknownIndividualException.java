package uk.org.mygrid.owlindividuals.api;

public class UnknownIndividualException extends BuilderException {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public UnknownIndividualException() {
		super();
	}

	public UnknownIndividualException(String message, Throwable cause) {
		super(message, cause);
	}

	public UnknownIndividualException(String message) {
		super(message);
	}

	public UnknownIndividualException(Throwable cause) {
		super(cause);
	}

}
